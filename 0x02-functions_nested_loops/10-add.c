/**
 * add - Adds two numbers
 *
 * @n: First number to be added
 * @m: Second number to be added
 *
 * Description: This function adds two numbers and returns the result.
 * The result is calculated using only the
 *
 * _putchar function and no standard library.
 * Return: The sum of n and m
 */
int add(int n, int m)
{
	int result = n + m;

	return (result);
}
